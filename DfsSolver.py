def moveLeft(self, posX, posY):
    posX[0] = posX[0] + 1
    posY[0] = posY[0] + 0


def moveRight(self, posX, posY):
    posX[0] = posX[0] - 1
    posY[0] = posY[0] + 0


def moveUp(self, posX, posY):
    posX[0] = posX[0] + 0
    posY[0] = posY[0] + 1


def moveDown(self, posX, posY):
    posX[0] = posX[0] + 0
    posY[0] = posY[0] - 1


